package 확인문제;

public class 확인문제2 {

	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		
		System.out.println("두 수를 더한 값은 얼마? " + (num1 + num2));
		System.out.println("두 수를 나눈 값은 얼마? " + (num1 / num2));
		System.out.println("두 수를 나눈 값의 나머지는 얼마? " + (num1 % num2));
	}

}
