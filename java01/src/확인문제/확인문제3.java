package 확인문제;

public class 확인문제3 {

	public static void main(String[] args) {
		double x = 33.3;
		double y = 22.2;
		
		System.out.println("x의 값은 44.2와 동일한가? " + (x == 44.2));
		System.out.println("x와 y의 값은 동일한가? " + (x == y));
		System.out.println("y의 값이 55보다 큰가? " + (y > 55));
	}

}
